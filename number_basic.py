from math import floor
from math import ceil
from math import sqrt

# 除法
print(8/5)

# 整數除法
print(8//5)

# 連續運算
print(8+8*5)
print((8+8)*5)

# 取餘數
print(8 % 5)

# 負數轉正數
print(abs(-8))

# 次方
print(pow(2, 4))

# 取最大值
print(max(2, 3, 4, 6))

# 四捨五入
print(round(4.6))

# 無條件捨去
print(floor(4.6))

# 無條件進位
print(ceil(4.6))

# 開根號
print(sqrt(16))
